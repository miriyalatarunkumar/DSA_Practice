/*
  reverse alternate nodes and append at the end
  The input list will have at least one element  
  Node is defined as 
  struct Node
  {
      int data;
      struct Node *next;
    
      Node(int x){
        data = x;
        next = NULL;
      }
    
   };

*/
class Solution
{
    public:
    void rearrange(struct Node *odd)
    {
        //add code here
        Node* head2 = odd->next;
        Node *p = odd, *q = head2;
        while(q && q->next){
            p->next=q->next;
            q->next=q->next->next;
            p=p->next;
            q=q->next;
        }
        if(p->next)p->next=NULL;//for even no. of nodes
        Node* r= NULL;
        q=NULL, p=head2;
        while(p){
            r=q;
            q=p;
            p=p->next;
            q->next=r;
        }
        head2=q;
        Node* tail = odd;
        while(tail->next) tail = tail->next;
        tail->next=head2;
    }
};



